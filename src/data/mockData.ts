export interface PVCalculationSteps {
  formula: string;
  marketRate: number;
  dealRate: number;
  notional: number;
  df: number;
  calculatedValue: number;
  unit: string;
}

export function calculatePV(deal: Deal, marketPrice: number): PVCalculationSteps {
  const notional = deal.buyAmt || 1000000;
  const dealRate = deal.rate || 1.0;
  const df = 0.9985; // Mock discount factor
  
  // Custom logic for illustrative formulas
  const formula = "PV = (Market_Rate - Deal_Rate) * Notional * DF";
  const calculatedValue = parseFloat(((marketPrice - dealRate) * notional * df / 1000).toFixed(2));
  
  return {
    formula,
    marketRate: marketPrice,
    dealRate,
    notional,
    df,
    calculatedValue,
    unit: 'k RUB'
  };
}
